    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String tipo = String.valueOf(txtTipo.getSelectedItem());
        if (validaCampos()) {
            if (tblVoluntarios.getSelectedRow() != -1){
                int id = (int) tblVoluntarios.getValueAt(tblVoluntarios.getSelectedRow(), 0);
                Voluntario v = voluntarioPorId(id);
                Evento e = new Evento(txtData.getText(), tipo, txtDescricao.getText(), txtHorario.getText(),v, txtEndereco.getText());
                e.setId(idEstatico);
                idEstatico++;
                e.setAuxTipo(txtTipo.getSelectedIndex());
                eventos.add(e);
                limpaCampos();
                geraLista();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Selecione um responsável.");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Preencha corretamente todos os campos.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

