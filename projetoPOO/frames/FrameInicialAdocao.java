    @Override
    public void geraLista() {
        DefaultTableModel model = new DefaultTableModel() {
            @Override
            public boolean isCellEditable (int row, int column) {
                return false;
            }
        };
        
        model.addColumn("ID");
        model.addColumn("Animal");
        model.addColumn("Adotante");
        model.addColumn("Data");

         // Preenche o modelo de tabela com os dados dos animais
        for (Adocao adocao : adocoes) {
            Object[] rowData = {adocao.getId(), adocao.getAnimal().getNome(), adocao.getAssociado().getNome(), adocao.getData()};
             model.addRow(rowData);
            
        }
        
        tblAdocoes.setModel(model);
        jScrollPane1.setViewportView(tblAdocoes);

        criaListaAnimais();
    }

